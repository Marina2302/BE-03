package module02.lesson11;

public class Person {
    private String name;
    private int age;
    private String company;

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    private String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }
}
